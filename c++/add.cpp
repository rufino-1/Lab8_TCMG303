#include <iostream>

using namespace std;

int main()
{
    int a;
    int b;
    cout << "This program takes two integers from the user, adds the integers, and outputs the result" << endl;
    
    cout << "Enter the first integer: ";
    cin >> a;
    
    cout << "Enter the second integer: ";
    cin >> b;
    
    cout << "The result of adding " << a << " and " << b << " is: " << a + b << endl;
    return 0;
}
